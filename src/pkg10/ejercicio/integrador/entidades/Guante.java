public class Guante {

    int base = 1;
    int tiempoDisparo = 1;
    private boolean estaRoto = false;

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }

    public int getTiempoDisparo() {
        return tiempoDisparo;
    }

    public void setTiempoDisparo(int tiempoDisparo) {
        this.tiempoDisparo = tiempoDisparo;
    }

    public boolean isEstaRoto() {
        return estaRoto;
    }

    public void setEstaRoto(boolean estaRoto) {
        this.estaRoto = estaRoto;
    }

   

}
